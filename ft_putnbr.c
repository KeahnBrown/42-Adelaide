#include "ft_printf.h"

int	ft_putnbr(int num)
{
	int	i;

	i = 0;
	if (num < 0)
	{
		if (num == -2147483648)
		{
			write(1, "-2147483648", 11);
			return (11);
		}
		num *= -1;
		i += ft_putchar('-');
	}
	return (i += ft_convert(num, 10, 0));
}
